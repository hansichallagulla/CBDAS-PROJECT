CREATE TABLE WaterConsumption (
    Country VARCHAR(100),
    Year INT,
    Total_Water_Consumption_Billion_Cubic_Meters FLOAT,
    Per_Capita_Water_Use_Liters_per_Day FLOAT,
    Agricultural_Water_Use FLOAT,
    Industrial_Water_Use FLOAT,
    Household_Water_Use FLOAT,
    Rainfall_Impact_Annual_Precipitation_in_mm FLOAT,
    Groundwater_Depletion_Rate FLOAT,
    Water_Scarcity_Level VARCHAR(50)
);